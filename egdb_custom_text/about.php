  <h1 style="font-size:32px;" class="text-center">Welcome to IHSM<span style="color:#9c5;">Subtropicals</span> Fruits Database</h1>

  <p style="font-size:18px;margin-top:20px;" class="text-justify">
    The Institute of Subtropical and Mediterranean Horticulture (<a href="https://www.ihsm.uma-csic.es/ihsm" target="_blank">IHSM</a>) "La Mayora" is a research center with more than 60 years of history and with a Research station of 50 Ha. Located in the province of Malaga, in southern Spain, it host a collection of varieties of subtropical species adapted to grow in the local environmental conditions (Mediterranean climate), outside of the tropics. The IHSM germplasm collection has the largest number of cherimoya acccessions of the world, and counts with more than 100 accessions of avocado and more than 80 of mango, among other species.
    <br>
    This site provides bioinformatics tools and genomics data of subtropical species maintained and studied at the IHSM.
  </p>
