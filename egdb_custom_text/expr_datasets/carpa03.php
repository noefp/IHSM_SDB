<p>
  The samples were obtained from the “Fujian Agriculture and Forestry University”. Female and male flowers of papaya were 
  sampled and fixed according to flower size and length and stored in FAA liquid. After putting samples through an alcohol
   dehydration series, flower samples were embedded in paraffin (LEICA EG 1150 H) in a Leica EG 1150 H paraffin embedding apparatus
    (Leica, Germany) at a melting point of 50-52 °C. Transverse sections with a thickness of 10 μm were made on a Leica automatic
     rotary microtome and stained with Brilliant Green (BIO-RAD). The sectioned and embedded papaya flowers were dissected under
      a dissecting microscope (Olympus, Japan) to isolate the different developmental stages of the male and female papaya flowers.
       Due to the difficulty in sampling pistils from male and female flowers at the primordium stage, the flower buds at the primordium
        stage was used as the experimental material for this study. 
</p>
