<p>
  The samples were obtained from the “University of Illinois at Urbana-Champaign”. Flower buds were collected from female and male ‘AU9’ papaya plants and
   hermaphrodite ‘SunUp’ plants grown at the Kunia Research Station of Hawaii Agriculture Research Center (HARC) in 2013. The flower buds were used to compare
    gene expression between sex types and obtain candidate sex determination genes by RNA-Seq. These flower buds were first classified according to their
     phenotype (sex) and then were divided into two groups according to their size (in millimeters). Flower buds were ground in liquid nitrogen for further
      RNA extraction. To further corroborate the differential expression of identified highly differentially expressed genes by qPCR, flower buds, and leaf
       tissue samples were collected again from three different ‘SunUp’ female plants, three different ‘SunUp’ hermaphrodite plants, three different ‘AU9’ 
       female plants and three different ‘AU9’ male plants grown at the Kunia Research Station of HARC during 2017. All samples were collected in Hawaii by 
       HARC, shipped in dry ice (−80C) to Urbana, Illinois and then ground in liquid nitrogen (− 196C) for further RNA extraction.
</p>
