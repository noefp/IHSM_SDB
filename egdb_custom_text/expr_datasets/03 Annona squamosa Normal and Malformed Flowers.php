<p>
  RNA-Seq of <i>Annona squamosa</i> (cv. Bendi) normal flowers and malformed flowers.
</p>

<h4>Experimental Conditions:</h4>

<ul>
  <li>Normal flowers.</li>
  <li>Malformed flowers.</li>
</ul>

<p>
The samples were obtained from the "Lingnan Normal University" in Guangdong, China. The flowers before open were collected from a normal flower and a malformed flower from <i>A. squamosa</i> trees. All of the flower samples were frozen immediately in liquid nitrogen, and stored at −80 ºC until use. Each sample of RNA was prepared by mixing three replicate samples.
</p>

