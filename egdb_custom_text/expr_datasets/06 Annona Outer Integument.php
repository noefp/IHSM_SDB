<p>
	RNA-Seq of pistils with and without early seed development in Atemoya (<i>Annona squamosa</i> × <i>A. cherimola</i>) cv. Gefner, and the <i>A. squamosa</i> "<i>Thai seedless</i>" mutant (<i>Annona ino</i>).
</p>

<h4>Experimental Conditions:</h4>

<ul>
	<li><b>AG</b> = Atemoya cv. Gefner<br>
	<li><b>T</b> = "<i>Thai seedless</i>" mutant<br>
	<li><b>PS</b> = Pistils containing early developing seeds (Pollinated pistils with seed).</li>
	<li><b>P</b> = Pistils without early developing seeds (Pollinated pistils, seed removed).</li>
</ul>

<p>
	Total RNA was extracted from pollinated pistils containing early seeds (PS) and pistils without early seeds (P) of <i>Annona cherimola</i> cv. Gefner (AG) and the "<i>Thai seedless</i>" mutant (T), combining the CTAB protocol by <a href="http://dx.doi.org/10.4236/ajps.2016.73037">Doyle and Doyle, 1987</a> and the Qiagen RNeasy Plant Mini Kit (Qiagen).
	<br>
	PS and P samples were collected from three independent fruits at 4 and 8 days after pollination (DAP). 
	Microscopic observations were performed at 8 DAP to illustrate early embryo and nucellus development.
</p>

