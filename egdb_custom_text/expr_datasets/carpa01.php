<p>
  The samples were obtained from the “Department of Food Science and Experimental Nutrition, School of Pharmaceutical Sciences, University of São Paulo”.
   Papayas (Carica papaya L. cv. “Golden”) at the pre-climacteric stage were acquired from a commercial producer in the municipality of Linhares/ES, Brazil,
    between one and two days after harvest, still with up to one-quarter of yellow peel (about 150 days after anthesis) and were harvested in three consecutive
     harvesting times (August, September, October 2017). Right after the fruit arrived in the laboratory and following sanitization with chlorine (100 ppm),
      some were immediately characterized and frozen to compose the 0-h control group. Remaining fruits were separated into two groups: treated and control.
       Control groups were left to ripen, and fruit samples were taken after 12 h and 24 h. Treatment with ethylene at 0 h was done by exposing randomly selected
        fruit to a concentration of 100 ppm (100 µLL−1) of ethylene, kept in constant flux for 17 min for gas saturation and 12 h more in a closed system.
         After 12 h, fruits were removed from the ethylene chamber and exhaustively air-vented for 1 h. Ethylene-treated fruits were frozen (12 h group), 
         and the other set of fruit was left to ripen as the control group for 12 more hours, totaling 24 h after fruits reached the laboratory (group 24 h treated).
          In all five groups (0 h, 12 h control, 24 h control, 12 h ethylene, and 24 h ethylene), papayas were characterized for ripening parameters and peeled;
           their seeds were removed, and the pulp was cut into cubes, frozen in liquid nitrogen, and stored at −80 ◦C. The experiment used three sets of harvested 
           fruit collected at three different times of the year (biological triplicate), and each group was composed of at least five fruits (which were pooled before
            storage at −80 ◦C).
</p>
