<p>
  The samples were obtained from the “Griffith University”. Two Australian commercial cultivars, RB1 and 1B, were harvested at ripening stages 1 and 3,
   based on the previously developed harvest ripening index, from a commercial papaya plantation in Mareeba, in June 2021. Three fruits of each variety at
    each fruit maturity stage were used as biological replicates. A total of 12 fruit samples were treated with Sportak to control postharvest disease,
     stored at 27 ◦C for one day and then transported in a refrigerated truck to the laboratory at Griffith University, Nathan Campus, Brisbane. Fruit
      flesh was cut into 5 mm pieces and flash frozen in liquid N2, then stored at −80 ◦C until processed for RNA extraction.
</p>
