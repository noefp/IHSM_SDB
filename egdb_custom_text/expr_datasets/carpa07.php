<p>
  The samples were obtained from the “Yucatan Center for Scientific Research”. After germination, the papaya plants were grown under greenhouse conditions
   and equivalently watered. Drought stress was imposed on three-month-old plants by withholding watering; healthy well-watered plants of same age were used
    as controls. Visual assessment of phenotype, physiological measurements, and sample collection for RNA isolation, were performed on plants under control
     condition and at 10 and 20 days after stress imposition.
</p>

