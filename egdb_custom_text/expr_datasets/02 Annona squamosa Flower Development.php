<p>
  RNA-Seq of <i>Annona squamosa</i> (cv. Bendi) flowers at different stages of development.
</p>

<h4>Experimental Conditions:</h4>

<ul>
  <li>Inflorescence meristeme.</li>
  <li>Flower bud.</li>
  <li>Mature flower with partially opened petals.</li>
  <li>Mature flower with opened and faded petals.</li>
</ul>

<p>
The samples were obtained from the "Lingnan Normal University" in Guangdong, China. The inflorescence meristem (IM), the flower buds (FB), and two stages of flowers (FL1 and FL2) were collected from <i>A. squamosa</i> trees. The flower buds were collected based on their size (3–6 mm). The two flower stages were the mature flowers with partially opened petals (FL1) and mature flowers with opened and faded petals (FL2). These tissue samples were frozen immediately in liquid nitrogen, and stored at −80 ºC until use. Equal volumes of RNA from each of the four stages of flower development were pooled, and each sample was prepared by mixing three replicate samples.
</p>

