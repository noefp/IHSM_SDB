  The samples were obtained from the “Fujian Agriculture and Forestry University” (FAFU). The papaya cultivar ‘Zhongbai’ from FAFU was used in this 
  study. All samples were grown in greenhouse of FAFU at a temperature of 28 ± 2 ◦C and 16 h light/8 h dark photoperiod. The light intensity is set 
