<p>
  The samples were obtained from the “Fujian Agriculture and Forestry University”. Gynodioecious papaya variety “Zhongbai” and dioecious papaya
   variety “Zhonghuang” were planted in the research station of Fujian Agriculture and Forestry University (FAFU). In November 2015, small inflorescences
    including rudimentary gynoecium of hermaphroditic dominant and auxiliary flowers, functional gynoecium of hermaphroditic dominant and female flowers 
    from “Zhongbai”, as well as, functional gynoecium of female flowers and rudimentary gynoecium of male flowers from “Zhonghuang” were collected near the 
    trunk apex and preserved in RNA later solution for 2 days. The ambient temperature fluctuated from 20 ˚C at night to 31 ˚C at day for these two days. 
    To investigate the differentially expressed genes between rudimentary and functional gynoecium, five comparisons were designed. Four comparisons were from 
    “Zhongbai” includes the functional gynoecium of hermaphroditic dominant flowers and rudimentary gynoecium of auxiliary flowers, functional gynoecium of 
    hermaphroditic dominant flowers and rudimentary gynoecium of hermaphroditic dominant flowers, functional gynoecium of female flower and rudimentary 
    gynoecium of hermaphroditic dominant flowers, and functional gynoecium of female flower and rudimentary gynoecium of hermaphroditic auxiliary flowers, 
    while, one was from dioecy variety “Zhonghuang” (functional gynoecium of female flower and rudimentary gynoecium of male flower). We selected early-stage 
    flowers with length &lt;2 mm and separated functional and rudimentary gynoecium by anatomic dissection under microscope for RNA extraction. Three biological 
    replications for each type of sample were collected with each replication of 5–30 flowers.
