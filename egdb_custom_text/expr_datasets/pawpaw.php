<p>
	RNA-Seq of pawpaw (<i>Asimina triloba</i>) early developing seeds collected from three cross-compatible (CC) 
  and three auto-incompatible (AI) pollinated flowers at 4, 8 and 15 days after pollination (DAP), resulting in a total of 18 biological replicates. 
  The pistils of each flower were dissected to obtain the developing seeds, and extract RNA from these fresh early seeds.
</p>

<h4>Experimental Conditions:</h4>

<ul>
  <li>Developing seeds of cross-compatible pollinated flowers at 4 DAP (CC4)</li>
  <li>Developing seeds of auto-incompatible pollinated flowers at 4 DAP (AI4)</li>
  <li>Developing seeds of cross-compatible pollinated flowers at 8 DAP (CC8)</li>
  <li>Developing seeds of auto-incompatible pollinated flowers at 8 DAP (AI8)</li>
  <li>Developing seeds of cross-compatible pollinated flowers at 15 DAP (CC15)</li>
  <li>Developing seeds of auto-incompatible pollinated flowers at 15 DAP (AI15)</li>
</ul>


