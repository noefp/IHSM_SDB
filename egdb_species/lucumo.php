<p>
  <em>Pouteria lucuma</em> (family Sapotaceae), commonly known as 'lucuma' or the 'Gold of the Incas', is a subtropical fruit native to the Andean regions of Peru, Chile, and Ecuador.
</p>

<ul>

  <li>
    <strong>Fruit and Floral Biology:</strong>
    The fruit is ovoid or elliptical in shape, measuring 7.5–10 cm in diameter, and is characterised by a thin, yellow-green skin and sweet, yellow-orange flesh. The small, greenish-white flowers grow in isolation or in fascicles and feature hairy sepals, a tubular corolla (1.0–1.8 cm) with five lobes, five stamens, five staminodes, a pubescent ovary, and a short style.
  <li>
    <strong>Alimentary Uses:</strong>  
    Lucuma is typically consumed overripe and is highly valued for its intense, maple-like flavour. While its distinct aftertaste makes fresh consumption less common, 
    it is a staple ingredient in Peruvian desserts, ice creams, and pastries. The fruit holds significant cultural importance in Peru, with "de seda" being the most premium 
    variety. Due to its high starch content, the pulp is often dried into a sweet, non-perishable, nutrient-dense flour (rich in iron, beta-carotene, and niacin), or frozen for 
    preservation. The tree's lightweight yet compact wood also has industrial and construction applications.
  <li>
    <strong>Cultivation and Harvest:</strong>  
    The tree thrives in temperate climates (20–22 °C) and is highly frost-sensitive. It prefers well-drained, sandy, nutrient-rich soils with neutral pH,
     while tolerating salinity and brief droughts. It does not withstand waterlogging or extreme heat. Optimal cultivation occurs around 500 m above sea level, yielding 200–300
      fruits per tree starting in the fourth or fifth year. Propagation by cuttings is preferred over seeds for higher yields.
  <li>
    <strong>Geographic Production:</strong>  
    Peru dominates global production (88%), followed by a growing industry in Chile (12%). Cultivation in the US has largely failed due to frost sensitivity, restricting its presence to regions like Hawaii, Costa Rica and Mexico. The fruit is primarily exported as frozen pulp (79%) and flour for the food industry.
  <li>
    <strong>Nutritional and Medicinal Importance:</strong>  
    Despite its sweet taste, lucuma has a relatively low sugar content—primarily glucose (8.4 g), fructose (4.7 g), and sucrose (1.7 g)—making it a healthy, natural sweetener
     alternative. It is an excellent source of soluble fibre, beta-carotene, niacin, and iron. The fruit exhibits high antioxidant
      capacity due to phenolic compounds, like catechin and epicatechin. Notably, its high concentration of phenolic compounds (11.4 mg/g DW) and strong α-glucosidase
       inhibitory activity suggest potential applications in diabetes management. Additionally, seed extracts have demonstrated antibacterial properties
        against <i>Staphylococcus aureus</i>, and contain compounds such as lupeol, amyrin, and the cyanogenic glycoside lucumin.
  </li>
</ul>

