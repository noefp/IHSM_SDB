<p>
  <em>Eriobotrya japonica</em> (loquat) is a diploid fruit species (2n = 2x = 34) belonging to the Rosaceae family, native to southeast China. It has been cultivated for over a thousand years in Japan and China, and has been commercially cultivated in more than 30 countries worldwide, including the United States, India, Indonesia, Iraq, Turkey, Spain, Australia, New Zealand, Pakistan, Brazil, Greece, Israel, Mexico, Argentina, France, Armenia, Syria and Italy.
</p>

<ul>

  <li>
    <strong>Floral and Fruit Biology:</strong>
    Loquat is a small evergreen fruit tree that blooms in autumn and early winter, and its fruit ripens in early summer requiring 4-5 months from blossom to fully ripe. Every inflorescence has 80-100 flowers, though only a small part of them will develop into fruits.
  </li>

  <li>
    <strong>Varieties and Characteristics:</strong>  
    Loquat cultivars can be divided into two groups based on their pulp and pericarp color: white- or yellow-fleshed. White-fleshed cultivars have higher sucrose contents, making them taste better. Yellow-fleshed fruit cultivars, which are the dominant cultivars, have higher nutrional value due to increased carotene contents. Some varieties, such as 'Wuheguoyu', have been described as seedless and triploid (2n = 3x = 51).
  </li>

  <li>
    <strong>Geographic Production:</strong>  
    Loquat was first domesticated 2000 years ago in China. Today, it has been cultivated in more than 30 countries, including Japan, the United States, France, Italy, Egypt, and Spain. Its annual yield exceeds 1.2 million tons worldwide. China is the largest producer of loquat fruit with a cultivation area of about 170,000 ha and an annual output of about one million tons.
  </li>

  <li>
    <strong>Nutritional and Medicinal Importance:</strong>  
    Loquat fruit is rich in dietary fiber and contains high amounts of vitamin A, carotenoids, antioxidants, ascorbic acid and many essential minerals including calcium, potassium, phosphorus, and magnesium. It is also chacaterized by the presence of triterpenes, flavonoids, sesquiterpene glycosides, megastigmane derivatives, phenylpropanoids, organic acids, and volatile oils.
  </li>
</ul>

