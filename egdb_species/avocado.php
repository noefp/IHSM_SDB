<p>
  <em>Persea americana</em> (avocado) is a diploid fruit species (2n=24) native to Mexico and Central America. It belongs to the Lauraceae family, which includes 50 genera and about 2,200 species, predominantly found in tropical and subtropical regions.
</p>

<ul>
  <li>
    <strong>Botanical Classification:</strong>  
    Avocado is classified into three botanical types based on origin and cultivation conditions:
    <ul>
      <li>
        <em>P. americana</em> cv. drymifolia (Mexican): Adapted to colder, mountainous regions.
      </li>
      <li>
        <em>P. americana</em> cv. guatemalensis (Guatemalan): Suited for colder climates, originating from high altitudes in Central America.  
      </li>
      <li>
        <em>P. americana</em> cv. american (Antillan): Prefers warmer, tropical lowlands.
      </li>
    </ul>
  </li>

  <li>
    <strong>Tree Characteristics:</strong>  
    Avocado trees are leafy and evergreen and can grow up to 20 meters tall. In commercial plantations, they are pruned to around 5 meters to facilitate harvesting and phytosanitary control.
  </li>

  <li>
    <strong>Floral Biology:</strong>  
    Avocado flowers grow in clusters and exhibit protogynous dichogamy, where they open first as female and later as male to enhance cross-pollination. The flowering pattern is classified as type A or B.
  </li>

  <li>
    <strong>Fruit Characteristics:</strong>  
    The avocado fruit is a single-seeded berry with variable peel colors (green, violet, or black), weights ranging from 150 to 350 grams, different shapes and yellow-green pulp. The fruits do not ripen on the tree; ripening occurs post-harvest and may take 6 to 12 months from flowering to maturity.
  </li>

  <li>
    <strong>Germplasm Conservation:</strong>  
    The <a href="https://www.ihsm.uma-csic.es/" target="_blank">Institute of Subtropical and Mediterranean Horticulture (IHSM)</a> conserves over 100 avocado accessions in its germplasm collection.
  </li>
</ul>

