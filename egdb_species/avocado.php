<p style="margin-top:20px">
    <i>Persea americana</i> Mill. (2n=24), commonly known as avocado, is native to Mexico and Central America (<a href="http://www.avocadosource.com/cas_yearbooks/cas_70_1986/cas_1986_pg_135-145.pdf" target="_blank">Bergh and Ellstrand, 1986</a>; <a href="https://www.mdpi.com/1420-3049/23/10/2421" target="_blank">Segovia et al., 2018)</a>. 
    It is a member of the <i>Lauraceae</i> family, which comprises 50 genus,
    and about 2200 species, most of which are tropical and subtropical (<a href="http://www.avocadosource.com/cas_yearbooks/cas_70_1986/cas_1986_pg_135-145.pdf" target="_blank">Bergh and Ellstrand, 1986</a>; <a href="https://link.springer.com/chapter/10.1007/978-3-662-02899-5_46" target="_blank">Rohwer, 1993</a>; <a href="https://onlinelibrary.wiley.com/doi/10.1002/bies.20721" target="_blank">Chanderbali et al., 2008</a>).
</p>
<p>
    The avocado is botanically classified into three types or races, named according to their origin: <i>Persea americana</i> var. <i>drymifolia</i> (mexican), <i>Persea americana</i> var. <i>guatemalensis</i> (guatemalan) and <i>Persea americana</i> var. <i>american</i> (antillan). 
    They present different cultivation conditions and fruit characteristics (<a href="http://www.avocadosource.com/cas_yearbooks/cas_70_1986/cas_1986_pg_135-145.pdf" target="_blank">Bergh and Ellstrand, 1986</a>; <a href="https://www.cabdirect.org/cabdirect/abstract/19876763104" target="_blank">Morton, 2004</a>; <a href="https://onlinelibrary.wiley.com/doi/10.1002/9780470958346.ch26" target="_blank">Zafar and Sidhu, 2011)</a>.
    The mexican and guatemalan subspecies are adapted to colder conditions (they originated from the montainous regions of Central America). 
    However, the antillan subspecies is adapted to warmer conditions (it originates from low-lying regions with tropical climates) (<a href="https://academic.oup.com/jhered/article/100/1/56/771306" target="_blank">Chen et al., 2009)</a>
</p>
<p>
    The avocado tree is leafy, evergreen and tall, it can reach up to 20 meters (<a href="https://www.scopus.com/record/display.uri?eid=2-s2.0-85019656909&origin=inward&txGid=1bbc067da2d0e5c82af0e5a241d0299b" target="_blank">Litz et al., 2007)</a>.
    However, those trees cultivated in comercial plantations do not usually allow them to grow more than 5 m to facilitate the practices of phytosanitary control, harvesting, pruning and foliar fertilization (<a href="https://www.academia.edu/30187106/Monograf%C3%ADa_del_aguacate_sagarpa" target="_blank">SAGARPA, 2011)</a>.  
</p>
<p>
    The flowers appear in clusters and have the particularity of opening at two different times: first as a female flower and then as a male flower, thus avoiding self-fertilization.
    They can be classified as type A or B, depending on the flowering pattern (<a href="https://www.academia.edu/30187106/Monograf%C3%ADa_del_aguacate_sagarpa" target="_blank">Litz et al., 2007)</a>.
</p>
<p>
    The avocado fruit is a berry with a single large seed (<a href="https://link.springer.com/article/10.1007/s10265-003-0136-0" target="_blank">Cowan and Wolstenholme, 2016)</a>, pear-shaped, the color varies from light to dark green and from violet to black, rough skin with a yellowish-green pulp. 
    There are approximately 400 varieties, so it can find fruits of different shapes and weights that can weigh from 150 to 350 g (<a href="https://es.scribd.com/document/370134679/Especies-Frutales-cultivadas-en-Cuba#" target="_blank">Rodríguez and Sánchez, 2005)</a>.
</p>
<p>
    Avocado fruits does not ripen on the tree. Ripening takes places after harvest (<a href="https://www.scopus.com/record/display.uri?eid=2-s2.0-66149093847&origin=inward&txGid=f3f10936fa59faac84aa5b28bd8b0c13" target="_blank">Blakey et al., 2009</a>; <a href="https://www.sciencedirect.com/science/article/abs/pii/B978184569734150008X?via%3Dihub" target="_blank">Yahia and Woolf, 2011)</a>.
    The development period of the fruit is quite long, from 6 to 12 months from flowering to maturity (<a href="https://scholar.google.com/scholar_lookup?title=The+avocado+%E2%80%93+botany,+production+and+uses&author=R.+Scora&author=B.+Wolstenholme&author=U.+Lavi&publication_year=2002&" target="_blank">Scora et al., 2002)</a>.
</p>
<p>
	The <a href="https://www.ihsm.uma-csic.es/" target="_blank">Institute of Subtropical and Mediterranean Horticulture (IHSM)</a> has over 100 accessions of avocado in its germplasm collection.
</p>



