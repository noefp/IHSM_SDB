<p>
  The pawpaw (<em>Asimina triloba</em> Dunal) is a diploid species (2n=18) belonging to the family Annonaceae, which is part of the Magnoliales order in the magnoliid clade. Unlike most members of the Annonaceae, which thrive in tropical and subtropical climates, <em>Asimina</em> is adapted to cold climates and is native to North America.
</p>

<ul>
  <li>
    <strong>Climatic Adaptation:</strong>  
    The pawpaw grows in USDA plant hardiness zones 5 (minimum −29&nbsp°C) through 8 (minimum −7&nbsp°C) and tolerates maximum temperatures above 35&nbsp°C. It is one of the few Annonaceae species adapted to temperate climates.
  </li>
  
  <li>
    <strong>Fruit Characteristics:</strong>  
    The pawpaw produces the largest edible fruit native to the USA. The fruit is climacteric, oblong in shape, with green skin that transitions to yellow as it ripens. The skin is soft and thin, limiting postharvest shelf life and transportation. To extend usability, the pulp is processed into products such as ice cream, jam, compote, and wine.
  </li>
  
  <li>
    <strong>Growth and Cultivation:</strong>  
    Pawpaw cultivation is mainly localized in the eastern United States at a limited commercial scale, predominantly for local consumption. There are over 40 commercial cultivars, indicating a niche market with potential for expansion.
  </li>
  
  <li>
    <strong>Floral Biology:</strong>  
    Pawpaw flowers are hermaphroditic and exhibit protogynous dichogamy, where stigmas become receptive before anther dehiscence, preventing self-pollination. The gynoecium contains 3–10 carpels, which develop into a cluster of fruits.
  </li>
  
  <li>
    <strong>Leaf Properties:</strong>  
    Pawpaw leaves are oblong-lanceolate and dark green in spring and summer, turning yellow before falling in autumn. They accumulate acetogenins, a class of compounds with antitumoral properties, as a defense mechanism against herbivory.
  </li>
  
  <li>
    <strong>Evolutionary and Economic Potential:</strong>  
    As an underutilized fruit crop, the pawpaw has significant potential for expansion in European countries with temperate climates. It also holds evolutionary importance due to its position among early-diverging angiosperms.
  </li>
</ul>

