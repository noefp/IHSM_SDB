<p>
  <i>Annona cherimola</i> is a diploid species (2n=2x=14), which originated in Mesoamerica. 
	It is a deciduous tree and it shows hermaphroditic flowers with protogynous dichogamy, recurrent in basal angiosperm.
	It is denominated as Cherimoya or "chirimoya" in South America and "Anona" in Central America as other closely related species. 
	In fact, this word was used by Linnaeus to name the genus Annona, but referred to it as the Latin Word "annona" which means "the harvest of a year". 
	This perennial fruit crop has been cultivated and extremely appreciated since pre-Columbian times of the Maya, Olmec or/and Izapa civilizations.
</p>
<p>
	Spain is the principal cherimoya commercial producer country with about 3,000 ha followed by Peru and Chile. 
	Although cherimoya is mainly cultivated in family orchards in Peru, Bolivia and Ecuador, 
	this crop is also cultivated at small scale in California (USA), Argentina, Madeira (Portugal), Ecuador, Bolivia, South Africa,
	Australia, Italy, Morocco, Algeria, Egypt and Israel.
</p>
<p>
	The <a href="https://www.ihsm.uma-csic.es/" target="_blank">Institute of Subtropical and Mediterranean Horticulture (IHSM)</a> comprehends the largest germplasm collection of varieties of <i>A. cherimola</i>, 
	with 334 accessions.
</p>
<p>
  Recently, the interest in this family has risen exponentially due to the presence of important compounds with medicinal properties, 
	some of them exclusive in the Annonaceae, as polyketide secondary metabolites acetogenins, presents especially in seed and leaves, 
	with antitumor, antimalarial and pesticidal properties.
</p>

