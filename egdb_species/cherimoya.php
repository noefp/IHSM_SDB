<p>
  <i>Annona cherimola</i> (cherimoya) is a diploid fruit species (2n=2x=14) native to Mesoamerica. It is denominated as cherimoya or "chirimoya" in South America and "anona" in Central America, as other closely related species. This species has been cultivated and appreciated since pre-Columbian times by the Maya, Olmec, and Izapa civilizations.
</p>

<ul>
  <li>
    <strong>Floral Biology:</strong>  
    Cherimoya flowers are hermaphroditic with protogynous dichogamy, where female and male structures mature at different times to prevent self-fertilization and enhance cross-pollination.
  </li>

  <li>
    <strong>Evolutionary Significance:</strong>
    The genus <i>Annona</i> belongs to the order Magnoliales, one of the earliest lineages of flowering plants. Its primitive floral characteristics and high genetic variability provide insights into the evolution of angiosperms and their adaptation to subtropical ecosystems.
  </li>

  <li>
    <strong>Geographic Production:</strong>
    Spain is the main commercial producer with 3,000 ha, followed by Peru and Chile. It is also cultivated on a smaller scale in California (USA), Argentina, Madeira (Portugal), South Africa, Australia, Italy, Morocco, Algeria, Egypt, and Israel.
  </li>

  <li>
    <strong>Germplasm Conservation:</strong>  
    The <a href="https://www.ihsm.uma-csic.es/" target="_blank">Institute of Subtropical and Mediterranean Horticulture "La Mayora" (IHSM)</a> holds the largest germplasm collection of <i>A. cherimola</i>, with more than 300 accessions.
  </li>

  <li>
    <strong>Medicinal Importance:</strong>  
    Cherimoya contains polyketide secondary metabolites, such as acetogenins, found in seeds and leaves. These compounds exhibit antitumor, antimalarial, and pesticidal properties.
  </li>
</ul>

