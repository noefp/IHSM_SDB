<p>
  <em>Feijoa sellowiana</em> (commonly known as feijoa, pineapple guava, or guavasteen) is a diploid (2n = 2x = 22), outcrossing evergreen shrub or small tree belonging to the Myrtaceae family. While distinct from the true guava (<i>Psidium guajava</i>), it is native to southern and southeastern Brazil, northeastern Argentina, and Uruguay.
</p>

<ul>

  <li>
    <strong> Floral and Fruit Biology:</strong>
    The plant produces conspicuous, bisexual flowers (approx. 4 cm in diameter) with fleshy petals—white externally and 
    purplish-red internally—and numerous prominent purple stamens with golden-yellow anthers. The fruit is an oblong or ovoid berry 
    (4–6 cm long) with a persistent calyx. Its peel is initially covered in a whitish bloom, maturing to a dull or yellowish-green. 
    Highly aromatic even before full ripeness, the fruit features thick, granular, watery flesh and a sweet, subacid, translucent central 
    pulp containing 20 to 100 small, inconspicuous seeds. 
    Its flavour profile is frequently described as a blend of pineapple, guava, and strawberry.</li>
  <li>
    <strong>Varieties and Characteristics:</strong>  
    As a monotypic species cultivated in high-altitude tropical and subtropical regions, feijoa is primarily categorized into 'Brazilian' and 
    'Uruguayan' types. To standardize classification, the International Union for the Protection of New Varieties
     of Plants (UPOV) has established a comprehensive list of descriptors based on more than 20 reference cultivars.
  <li>
    <strong>Geographic Production:</strong>  
    Although native to South America, feijoa was globally distributed in the late 19th century by botanist Edouard André. In its native Uruguay,
     commercialization and genetic improvement programs—led by INIA and Fagro-Udelar to enhance agronomic traits and fruit quality—have significantly 
     expanded domestic sales in recent years. Globally, Colombia is the leading commercial producer, yielding 3,133 tons across 339 hectares in 2022, 
     followed by New Zealand with an annual production of 1,100 tons. Other notable producing regions include California, Georgia, Portugal, Italy,
      Australia, Brazil, and Azerbaijan.

  <li>
    <strong>Nutritional and Medicinal Importance:</strong>  
    Feijoa is highly nutritious, providing dietary fibre, pectin, vitamin C (28 mg/100 g fresh weight), and essential minerals including iodine (3 mg/100 g),
     potassium, phosphorus, magnesium, and calcium. Its distinct aroma derives from volatile esters, primarily methyl benzoate, ethyl benzoate, and ethyl butanoate.
      The fruit is rich in bioactive phytochemicals, notably polyphenols (such as flavones, catechins, quercetin-glycosides, and proanthocyanidins), quinones,
       terpenes, tannins, and steroid saponins. Research indicates these compounds impart significant therapeutic properties, including antibacterial and 
       antifungal activities, as well as acetylcholine and butyrylcholine esterase inhibition.
</ul>
