<p>
  <em>Averrhoa carambola</em> (carambola or starfruit) is a tropical fruit species native to Southeast Asia and belongs to the family Oxalidaceae. It is valued for its distinctive star-shaped cross-section, sweet-tart flavor, and high juice content, making it a popular fruit in tropical regions and beyond.
</p>

<ul>
  <li>
    <strong>Floral and Fruit Characteristics:</strong>  
    The fruit develops through distinct color indices that indicate its ripeness: green (unripe), yellow (ripe), and golden yellow (overripe). Carambola has a juicy, succulent flesh with a sweet and slightly acidic flavor, making it suitable for fresh consumption or juice production.
  </li>

  <li>
    <strong>Geographic Production:</strong>  
    Malaysia is the largest exporter of carambola globally, exporting over 90% of its production primarily to Europe. Other significant producers include India, the Philippines, and other Southeast Asian countries. Malaysia's annual production is valued at 20â€“30 million MYR, with exports steadily increasing since 1989.
  </li>

  <li>
    <strong>Phytochemical and Antioxidant Properties:</strong>  
    Carambola contains alkaloids, flavonoids, tannins, and high levels of polyphenolic antioxidants such as L-ascorbic acid, epicatechin, and gallic acid. These compounds contribute to its health benefits, including antioxidant activity. Notably, the byproducts of juice extraction (pomace) have higher antioxidant activity than the juice itself, offering potential for functional food applications.
  </li>

  <li>
    <strong>Traditional and Medicinal Uses:</strong>  
    Starfruit has been used in traditional medicine for treating various ailments. Its rich phytochemical profile supports its role in promoting health and as an ingredient in functional products.
  </li>

  <li>
    <strong>Economic Importance:</strong>  
    Carambola is a significant commodity in tropical fruit markets. Its export value grew by 2.3% annually from 1992 to 2002, with Malaysia ranking third in fruit export value during this period. The increasing global demand for tropical juices has further boosted its economic significance.
  </li>
</ul>
